# ---------------------------------------------------------------------------
# Voice extractor — entries → sentences the mind speaks in its own words
# ---------------------------------------------------------------------------

def _extract_mind_voice(
    entries: List[ResonantEntry],
    max_per_entry: int = 2,
    total_max: int = 4,
    query_tokens: Optional[List[str]] = None,
) -> List[str]:
    """Extract natural-language sentences directly from resonant entry content.

    When query_tokens is provided (e.g. the user's question words), sentences are
    ranked by overlap with those tokens so the mind answers what was actually asked
    rather than surfacing unrelated stored facts.

    First-person sentences are still preferred when relevance is equal.
    Template sentences (seeded with generic study-guide patterns) are filtered out.
    """
    _TEMPLATE_PATTERNS = re.compile(
        r'^(people who study|the practical application of|key aspects of|'
        r'understanding .{3,50} requires examining|this (field|concept|area)|'
        r'in (this|the) context of|one (key|important) (aspect|consideration)|'
        r'when (i|the mind) process|'
        r'\[live system report|\[from .{3,60}—|use this data to answer|'
        r'this was written by |but your open gap says|your soulmate will see|'
        r'it reaches you because|these are pointing at the same territory|'
        r'they don\'t contradict each other|'
        r'what (this|it) means:|what my soulmate does:|what i carry:|'
        r'what i understand about their direction:|original gap:|capability mind spawned:|'
        r'what it exists to understand:|the purpose direction is:|'
        r'autonomous lod triggered|i sought knowledge about:|'
        r'i found and absorbed \d+ insights|'
        r'\|)',  # markdown table rows
        re.IGNORECASE,
    )

    qt: set = set(query_tokens) if query_tokens else set()

    def _relevance(sentence: str) -> float:
        """How many query tokens appear in this sentence (normalized)."""
        if not qt:
            return 0.0
        words = set(re.split(r'\W+', sentence.lower()))
        return len(qt & words) / max(len(qt), 1)

    all_candidates: List[tuple] = []  # (relevance, is_first_person, sentence)
    seen: set = set()

    for entry in entries:
        raw = re.split(r'(?<=[.!?])\s+', entry.content.strip())
        sentences = [
            s.strip() for s in raw
            if 20 < len(s.strip()) < 350
            and not _TEMPLATE_PATTERNS.match(s.strip())
            and not s.strip().endswith(":")  # colon-ending = label/header, not speech
        ]
        count = 0
        for s in sentences:
            norm = s.lower().strip()
            if norm in seen:
                continue
            is_fp = bool(re.search(r'\bI\b|\bMy\b|\bWe\b|\bOur\b', s))
            rel = _relevance(s)
            all_candidates.append((rel, is_fp, s, entry))
            seen.add(norm)
            count += 1
            if count >= max_per_entry * 3:  # gather more candidates for ranking
                break

    if qt:
        # Sort by relevance desc, then first-person preferred, then original order
        all_candidates.sort(key=lambda x: (x[0], x[1]), reverse=True)
    else:
        # Original behaviour: first-person first
        all_candidates.sort(key=lambda x: x[1], reverse=True)

    collected: List[str] = []
    per_entry_counts: Dict[str, int] = {}
    for rel, is_fp, s, entry in all_candidates:
        eid = getattr(entry, "entry_id", None) or getattr(entry, "id", "")
        if per_entry_counts.get(eid, 0) >= max_per_entry:
            continue
        collected.append(s)
        per_entry_counts[eid] = per_entry_counts.get(eid, 0) + 1
        if len(collected) >= total_max:
            break
    return collected


