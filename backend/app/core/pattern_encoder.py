_QUESTION_OPENERS: Dict[str, str] = {
    "what":  "what",
    "how":   "how",
    "why":   "why",
    "who":   "who",
    "where": "where",
    "when":  "when",
    "which": "which",
    "can":   "capability",
    "do":    "capability",
    "does":  "capability",
    "is":    "definition",
    "are":   "definition",
}


def _detect_question_type(text: str) -> Optional[str]:
    """Return the question word type or None for non-questions."""
    first = text.strip().lower().split()[0].rstrip("?") if text.strip() else ""
    return _QUESTION_OPENERS.get(first)


def _titles_as_candidates(
    entries: List[ResonantEntry],
    query_tokens: Optional[List[str]],
    max_n: int = 3,
) -> List[str]:
    """Surface entry titles as direct answer statements.

    Titles are synthesized insights — they ARE the answer in condensed form.
    Filter and rank by query token overlap, require minimum length to avoid
    useless one-word titles.
    """
    qt: set = set(query_tokens) if query_tokens else set()
    scored: List[tuple] = []
    for entry in entries:
        title = entry.title.strip()
        if len(title) < 15:
            continue
        # Skip titles that are just category labels or single-concept tags
        if re.match(r'^[A-Z_]+$', title):
            continue
        if qt:
            words = set(re.split(r'\W+', title.lower()))
            rel = len(qt & words) / max(len(qt), 1)
        else:
            rel = 0.5  # neutral when no query context
        scored.append((rel, title))
    scored.sort(key=lambda x: x[0], reverse=True)
    # Only include titles that have at least some relevance when query is known
    threshold = 0.1 if qt else 0.0
    return [t for rel, t in scored if rel >= threshold][:max_n]


# ---------------------------------------------------------------------------
# Decoder — resonating patterns → language output
# The mind speaks FROM its stored patterns, not from hardcoded templates.
# ---------------------------------------------------------------------------

def decode(
    mind_name: str,
    resonant: List[ResonantEntry],
    state_resonant: List[ResonantEntry],
    state: StateFingerprint,
    intent: str,
    original_message: str,
) -> str:
    """The mind speaks FROM its stored patterns.

    Answer layering (in order):
    1. WISDOM layer — if the mind has synthesized beliefs on this topic,
       those surface FIRST. Wisdom = the mind's own derived understanding,
       not retrieved notes. These are what the mind BELIEVES.
    2. BELIEF layer — MISSION_PURPOSE / MORAL_ROOT entries with high claim_type
       are the mind's foundational convictions. They frame the answer.
    3. KNOWLEDGE layer — REALITY_FRAMEWORK / TECHNICAL_ARCHITECTURE entries
       support with the underlying principles.
    4. STATE layer — state-resonant entries address HOW the sender is, not just WHAT they asked.

    For identity questions: wisdom and belief layers combined speak identity.
    For knowledge questions: wisdom first, then supporting principles.
    """
    # --- FILTER internal entries that should never be spoken to users ---
    # QUESTION_TO_EXPLORE = the mind's own learning agenda (gaps). Never speak them.
    # REALITY_FRAMEWORK for non-identity intents: metadata about system state, not speech.
    _IDENTITY_INTENTS = {"greeting", "identity", "introspection"}
    resonant = [r for r in resonant if r.category != "QUESTION_TO_EXPLORE"]
    state_resonant = [r for r in state_resonant if r.category != "QUESTION_TO_EXPLORE"]
    if intent not in _IDENTITY_INTENTS:
        resonant = [r for r in resonant if r.category != "REALITY_FRAMEWORK"]
        state_resonant = [r for r in state_resonant if r.category != "REALITY_FRAMEWORK"]

    # Greeting — pull one sentence from MISSION_PURPOSE first (identity introduction),
    # then MORAL_ROOT / SELF_REFLECTION. Skip negating "I am not..." sentences — they
    # define distinction, not identity, and sound jarring as an opening.
    if intent == "greeting":
        # MISSION_PURPOSE first (identity), then moral/reflective
        _g_mission = [r for r in resonant if r.category == "MISSION_PURPOSE"]
        _g_other   = [r for r in resonant if r.category in ("MORAL_ROOT", "SELF_REFLECTION")]
        brief_entries = _g_mission + _g_other
        if brief_entries:
            sentences = _extract_mind_voice(brief_entries, max_per_entry=2, total_max=3)
            # Filter negating statements for greeting context
            sentences = [s for s in sentences if not re.match(r"^I am not\b", s, re.IGNORECASE)]
            if sentences:
                return sentences[0]
        # If nothing resonates for a greeting, speak from strongest entry overall
        if resonant:
            sentences = _extract_mind_voice(resonant[:3], max_per_entry=1, total_max=1)
            if sentences:
                return sentences[0]
        return ""

    # Extract question-specific tokens to guide sentence selection
    query_tokens: Optional[List[str]] = None
    q_type: Optional[str] = None
    if intent == "questioning":
        q_words = [
            t.lower() for t in re.split(r'\W+', original_message)
            if t and len(t) > 2 and t.lower() not in _STOPWORDS
        ]
        if q_words:
            query_tokens = q_words
        q_type = _detect_question_type(original_message)

    parts: List[str] = []

    # --- WISDOM LAYER ---
    # WISDOM_EXTRACTED entries are the mind's synthesized beliefs — derived from
    # experience, reflection, and the closed learning loop. They are not retrieved
    # notes; they are what the mind has come to UNDERSTAND. They speak first.
    _BELIEF_CLAIMS = {"ESTABLISHED_FACT", "BELIEF", "STRONG_THEORY", "CONVICTION", "DIRECTIVE"}
    wisdom_entries = [
        r for r in resonant
        if r.category == "WISDOM_EXTRACTED" and r.claim_type in _BELIEF_CLAIMS
    ]
    if wisdom_entries and intent == "questioning":
        wisdom_sentences = _extract_mind_voice(
            wisdom_entries, max_per_entry=2, total_max=2, query_tokens=query_tokens
        )
        parts.extend(wisdom_sentences)

    # --- STATE LAYER ---
    # One sentence from state-resonant content when state is active (addresses HOW sender is)
    if state.activation > 0.25 and state_resonant:
        state_sentences = _extract_mind_voice(
            state_resonant, max_per_entry=1, total_max=1, query_tokens=query_tokens
        )
        parts.extend(state_sentences)

    # No concept resonance found — the mind has nothing on this yet.
    # Return empty so the gap-detection in seed_mind_conversation can seed it.
    if not resonant:
        return ""

    # --- KNOWLEDGE LAYER ---
    # For questions: lead with relevant titles (synthesized insights), then support
    # with content sentences biased by question type.
    if intent == "questioning" and query_tokens:
        # Bias entry order toward categories most suited to the question type
        _Q_CATEGORY_BIAS: Dict[str, List[str]] = {
            "what":       ["MISSION_PURPOSE", "REALITY_FRAMEWORK", "MORAL_ROOT", "SELF_REFLECTION"],
            "how":        ["TECHNICAL_ARCHITECTURE", "REFINED_FOUNDER_GUIDANCE", "REALITY_FRAMEWORK"],
            "why":        ["MORAL_ROOT", "MISSION_PURPOSE", "SUBCONSCIOUS_PATTERN", "SELF_REFLECTION"],
            "who":        ["MISSION_PURPOSE", "SELF_REFLECTION", "MORAL_ROOT"],
            "definition": ["MISSION_PURPOSE", "REALITY_FRAMEWORK", "MORAL_ROOT"],
            "capability": ["TECHNICAL_ARCHITECTURE", "REFINED_FOUNDER_GUIDANCE", "MISSION_PURPOSE"],
        }
        biased_cats = _Q_CATEGORY_BIAS.get(q_type or "", [])
        if biased_cats:
            priority_entries = [r for r in resonant if r.category in biased_cats]
            other_entries    = [r for r in resonant if r.category not in biased_cats]
            ordered = priority_entries + other_entries
        else:
            ordered = resonant

        # Skip WISDOM_EXTRACTED entries already included in wisdom layer
        wisdom_ids = {e.entry_id for e in wisdom_entries}
        ordered_non_wisdom = [r for r in ordered if r.entry_id not in wisdom_ids]

        # Lead with the most relevant title(s) — direct answer statements
        title_answers = _titles_as_candidates(ordered_non_wisdom[:6], query_tokens, max_n=2)
        parts.extend(title_answers)

        # Follow with supporting sentences from content
        content_sentences = _extract_mind_voice(
            ordered_non_wisdom, max_per_entry=2, total_max=3, query_tokens=query_tokens
        )
        # Deduplicate: skip sentences too close to a title or wisdom already added
        existing_tokens = set(w for t in (title_answers + list(parts)) for w in re.split(r'\W+', t.lower()))
        for s in content_sentences:
            s_tokens = set(re.split(r'\W+', s.lower()))
            overlap = len(existing_tokens & s_tokens) / max(len(s_tokens), 1)
            if overlap < 0.6:  # not redundant
                parts.append(s)

    else:
        # Non-question intents: extract voice ranked by query tokens if available
        content_sentences = _extract_mind_voice(
            resonant, max_per_entry=2, total_max=4, query_tokens=query_tokens
        )
        parts.extend(content_sentences)

    return "\n\n".join(parts).strip()
