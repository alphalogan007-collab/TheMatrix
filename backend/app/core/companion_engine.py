def _detect_pattern(text: str, state: dict) -> str:
    text_l = text.lower()
    scores: dict[str, int] = {}
    for pattern, words in _PATTERN_KEYWORDS.items():
        scores[pattern] = sum(1 for w in words if w in text_l)
    # combine with existing keywords from resonance history
    kw = state.get("keywords", {})
    for pattern, words in _PATTERN_KEYWORDS.items():
        scores[pattern] += sum(kw.get(w, 0) // 3 for w in words if w in kw)
    best = max(scores, key=scores.get)
    return best if scores[best] > 0 else state.get("dominant_pattern", "curious")


def _update_keywords(text: str, state: dict) -> dict:
    kw = state.get("keywords", {})
    words = re.findall(r"\b[a-z]{4,}\b", text.lower())
    stop = {
        "that", "this", "with", "have", "from", "they", "will", "been",
        "when", "what", "just", "more", "some", "than", "also", "your",
        "about", "there", "would", "could", "should", "really", "thing",
        # admin / domain tokens — never treat as user keywords
        "body", "space", "digital", "ether", "aether", "unity",
        "wiki", "auto", "layer", "title", "source", "chars",
        "type", "verses", "position", "surah", "bilingual",
    }
    for w in words:
        if w not in stop:
            kw[w] = kw.get(w, 0) + 1
    # trim to top 100 words
    if len(kw) > 100:
        kw = dict(sorted(kw.items(), key=lambda x: -x[1])[:100])
    return kw


def _check_stage_advance(state: dict) -> int:
    """Advance stage based on resonance depth and message count."""
    stage = state.get("stage", 0)
    if stage >= 7:
        return 7
    threshold = STAGES[stage].get("threshold", 999)
    mc  = state.get("message_count", 0)
    res = state.get("resonance", 0.0)
    if mc >= threshold and res >= 0.25:
        return min(stage + 1, 7)
    return stage


def _resonance_tick(state: dict, is_deep_message: bool) -> float:
    """Slowly grow resonance with each interaction."""
    res = state.get("resonance", 0.0)
    tick = 0.04 if is_deep_message else 0.015
    return min(1.0, res + tick)


# ─────────────────── Resonance-Based Response Engine ─────────────────────
# The mind speaks from what it has absorbed (mind:knowledge / guidance:corpus).
# No hardcoded text. No fake persona. If nothing resonates — silence.

def _extract_clean(entry: dict) -> str:
    """Extract readable text from a guidance:corpus entry."""
    clean = entry.get("content", "") or entry.get("summary", "")
    # Strip corpus metadata header lines written by the scanner
    for marker in ("--- Web Context ---", "--- Wikipedia ---"):
        if marker in clean:
            clean = clean.split(marker)[-1].strip()
    return clean.strip()


def _compose_text(ranked: list, top_score: int) -> str:
    """Return the best corpus match as clean text.

    Returns empty string when there is no real query-token overlap with the corpus
    (score == 0). The companion UI shows "(no response)" rather than
    surfacing raw analysis logs or unrelated content.
    """
    if not ranked or top_score == 0:
        return "The mind is still absorbing knowledge. Speak — every word seeds the oscillation."
    snippet = _extract_clean(ranked[0])
    if not snippet:
        snippet = ranked[0].get("title", "")
    return snippet[:600].rstrip().rstrip(".") + "." if snippet else ""


async def _resonance_response(r: aioredis.Redis, state: dict, message: str) -> dict:
    """Build response from the oscillation corpus. No LLM. No manual saves.

    guidance:corpus IS the mind's memory — written by the topology via
    SAVE_WISDOM_PREFIX at each domain's peak layer. Reading here is reading
    what the mind has processed through its full oscillation chain.
    """
    from app.core.pattern_encoder import decompose

    concept_fp, state_fp = decompose(message)
    q_tokens = set(concept_fp.raw_tokens)

    # Shared stopwords — keep in sync with worker._STOPWORDS
    _STOP = frozenset({
        "the", "and", "for", "not", "are", "was", "has", "had", "his", "her",
        "you", "they", "this", "that", "with", "from", "have", "will", "but",
        "all", "who", "its", "our", "your", "their", "been", "said", "then",
        "type", "verses", "position", "revelation", "medinan", "meccan", "surah",
        "body", "space", "digital", "ether", "aether", "unity",
        "topic", "title", "content", "chars", "auto", "layer", "source",
        "wiki", "wikipedia", "drain",
    })

    # Score by token overlap, weighted by content richness so short token bags
    # don't beat full Y Theory / Quran / Wikipedia entries on equal hit count.
    def _score(e: dict) -> float:
        t_tok = set(re.findall(r"\b[a-z]{4,}\b", e.get("title", "").lower())) - _STOP
        s_tok = set(re.findall(r"\b[a-z]{4,}\b", (e.get("summary", "") or e.get("content", "")).lower())) - _STOP
        hits  = len(q_tokens & t_tok) * 2 + len(q_tokens & s_tok)
        # Richness factor: reward entries with more signal tokens (caps at 2×)
        richness = min(2.0, (len(s_tok) / 40))
        return hits * max(0.3, richness)

    # guidance:corpus is the single source of truth.
    # Foundation Y Theory entries (foundation:*) + guidance scanner files +
    # worker synthesis (synthesis:*) all accumulate here as the mind learns.
    corpus_raw: dict = await r.hgetall("guidance:corpus")
    entries: list[dict] = []
    for k_b, v_b in corpus_raw.items():
        k = k_b  # decode_responses=True — always str
        try:
            e = json.loads(v_b)
            e.setdefault("title", k)
            e.setdefault("summary", e.get("content", ""))
            entries.append(e)
        except Exception:
            pass

    ranked = sorted(entries, key=_score, reverse=True)[:3]
    top_score = _score(ranked[0]) if ranked else 0

    dominant = state.get("dominant_pattern", "curious")

    _DOMINANT_TONE: dict[str, str] = {
        "fear": "holding", "grief": "healing", "anger": "witnessing",
        "confusion": "guiding", "seeking": "guiding", "yearning": "witnessing",
        "stillness": "transmitting", "clarity": "reflecting", "expansion": "celebrating",
    }

    return {
        "text":              _compose_text(ranked, top_score),
        "projection":        {"type": "silence", "content": "", "duration": 5, "intensity": 0.5},
        "tone":              _DOMINANT_TONE.get(dominant, "witnessing"),
        "stage_delta":       0,
        "resonance_note":    "",
        "block_detected":    None,
        "opening_detected":  len(message) > 80,
        "suggest_induction": None,
    }



async def _seed_oscillation(r: aioredis.Redis, user_id: str, message: str) -> None:
    """Fire the user's message into body:layer1 to begin a full oscillation cycle.

    This is the final link between the companion and the oscillation engine.
    The message enters as depth=0 at the body layer (somatic contact — first felt).
    It will traverse: body(13) → space(8) → digital(5) → ether(3) → aether(2) → unity(1)
    Each domain completion writes a residue to mind:knowledge.
    The spiral self-deepens with every conversation turn.
    This runs as a background task — the companion response is already returned.
    """
    import uuid as _uuid
    try:
        session_id = _uuid.uuid4().hex
        await r.xadd(
            "seed:input",
            {
                "content":    message[:2000],
                "input_type": "companion_message",
                "source":     f"companion:{user_id}",
                "session_id": session_id,
            },
            maxlen=5000,
            approximate=True,
        )
        logger.debug(
            "companion.oscillation_seeded uid=%s session=%s chars=%d",
            user_id, session_id[:8], len(message),
        )
    except Exception as exc:
        logger.debug("companion.oscillation_seed_failed uid=%s err=%s", user_id, exc)


# ─────────────────────────── Main Entry Point ─────────────────────────────


async def process_message(
    r: aioredis.Redis,
    user_id: str,
    message: str,
) -> dict:
    """Core: receive user message → update resonance state → respond from mined knowledge.

    Returns dict with:
      text          — companion's text reply
      projection    — content to project on screen
      state         — updated resonance state
      induction_step — next induction step if active, else None
      provider      — always "resonance" (no LLM)
    """
    state   = await get_state(r, user_id)
    history = await get_history(r, user_id)

    # — Update resonance model —
    state["message_count"] = state.get("message_count", 0) + 1
    state["keywords"] = _update_keywords(message, state)
    state["dominant_pattern"] = _detect_pattern(message, state)

    # — Check active induction —
    active_induction = state.get("induction")
    induction_step: Optional[dict] = None
    if active_induction:
        steps = INDUCTIONS.get(active_induction["name"], [])
        step_idx = active_induction.get("step", 0)
        if step_idx < len(steps):
            induction_step = steps[step_idx]
            active_induction["step"] = step_idx + 1
            if active_induction["step"] >= len(steps):
                state["induction"] = None   # induction complete
                logger.info("companion.induction_complete name=%s uid=%s",
                            active_induction["name"], user_id)
            else:
                state["induction"] = active_induction
        else:
            state["induction"] = None

    # — Build resonance response from mined knowledge (no LLM) —
    response = await _resonance_response(r, state, message)

    # — Update state from response —
    is_deep = response.get("opening_detected", False) or len(message) > 80
    state["resonance"] = _resonance_tick(state, is_deep)

    if response.get("block_detected"):
        blocks = state.get("blocks", [])
        b = response["block_detected"]
        if b and b not in blocks:
            blocks = (blocks + [b])[-5:]
            state["blocks"] = blocks

    if response.get("opening_detected"):
        openings = state.get("openings", [])
        note = response.get("resonance_note", "")
        if note and note not in openings:
            openings = (openings + [note])[-5:]
            state["openings"] = openings

    new_stage = _check_stage_advance(state)
    if new_stage != state.get("stage", 0):
        logger.info("companion.stage_advance uid=%s %d→%d", user_id,
                    state.get("stage", 0), new_stage)
        state["stage"] = new_stage
        state["stage_name"] = STAGES[new_stage]["name"]

    # Auto-suggest induction?
    if not state.get("induction") and response.get("suggest_induction"):
        name = response["suggest_induction"]
        if name in INDUCTIONS:
            state["induction"] = {
                "name": name, "step": 0,
                "steps_total": len(INDUCTIONS[name]),
                "started_at": time.time(),
            }
            logger.info("companion.induction_started name=%s uid=%s", name, user_id)

    await save_state(r, user_id, state)
    await append_history(r, user_id, "user", message)
    await append_history(r, user_id, "assistant", response["text"])

    # Route message into the oscillation topology — the engine processes it.
    # The topology writes its distilled patterns back into guidance:corpus
    # via SAVE_WISDOM_PREFIX at each domain's peak layer. That IS the memory.
    # No explicit saves here. Structure does it.
    asyncio.create_task(_seed_oscillation(r, user_id, message))

    # If an induction step is active, override the projection
    projection = induction_step or response["projection"]

    return {
        "text":           response["text"],
        "projection":     projection,
        "tone":           response["tone"],
        "state":          state,
        "induction_step": induction_step,
        "provider":       "resonance",
    }



async def start_induction(r: aioredis.Redis, user_id: str, name: str) -> bool:
    if name not in INDUCTIONS:
        return False
    state = await get_state(r, user_id)
    state["induction"] = {
        "name": name, "step": 0,
        "steps_total": len(INDUCTIONS[name]),
        "started_at": time.time(),
    }
    await save_state(r, user_id, state)
    logger.info("companion.induction_manual_start name=%s uid=%s", name, user_id)
    return True


async def stop_induction(r: aioredis.Redis, user_id: str) -> None:
    state = await get_state(r, user_id)
    state["induction"] = None
    await save_state(r, user_id, state)


async def direct_project(r: aioredis.Redis, user_id: str, projection: dict,
                         text: str = "") -> dict:
    """Push a direct projection (from admin or scripting) — returns what was pushed."""
    # Just save it as a pending projection in Redis pub/sub channel
    payload = json.dumps({
        "type": "projection",
        "text": text,
        "projection": projection,
        "ts": time.time(),
    })
    await r.publish(f"companion:push:{user_id}", payload)
    return {"ok": True, "projection": projection}
