def _entry_concept_domains(entry) -> Dict[str, float]:
    combined = f"{entry.title} {entry.content} {getattr(entry, 'tags', '') or ''}"
    fp = encode(combined)
    affinity = CATEGORY_AFFINITY.get(entry.category, {})
    scores = dict(fp.domains)
    for domain, boost in affinity.items():
        scores[domain] = scores.get(domain, 0.0) * boost
    return scores


def resonate(
    fingerprint: ConceptFingerprint,
    entries: list,
    top_n: int = 5,
    primary_mind: Optional[str] = None,
) -> List[ResonantEntry]:
    """Score stored patterns against the language fingerprint (concept resonance).

    primary_mind: when set, entries belonging to this mind get a 2× boost so the
    mind's own specific knowledge surfaces above the inherited seed_mind base pool.
    """
    scored: List[Tuple[float, object]] = []
    for entry in entries:
        entry_fp = _entry_concept_domains(entry)
        dot = sum(fingerprint.domains.get(d, 0.0) * w for d, w in entry_fp.items())
        boost = CLAIM_BOOST.get(getattr(entry, "claim_type", "") or "", 1.0)
        # Own-mind boost: the mind's own entries carry 2× weight vs inherited base
        if primary_mind and getattr(entry, "mind_name", None) == primary_mind:
            boost *= 2.0
        score = dot * boost
        if score > 0:
            scored.append((score, entry))
    scored.sort(key=lambda x: x[0], reverse=True)
    return [
        ResonantEntry(
            entry_id=e.id,
            mind_name=e.mind_name,
            category=e.category,
            title=e.title,
            content=e.content,
            claim_type=getattr(e, "claim_type", "") or "",
            tags=getattr(e, "tags", "") or "",
            resonance=score,
        )
        for score, e in scored[:top_n]
    ]


