# Domain list — fixed order for bitmask encoding (NEVER reorder)
# ---------------------------------------------------------------------------

_DOMAIN_LIST: List[str] = list(CONCEPT_DOMAINS.keys())
_BASE62_CHARS = "0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz"


# ---------------------------------------------------------------------------
# Language encoder — text → ConceptFingerprint
# ---------------------------------------------------------------------------

def _concept_hash(dominant_domains: List[str]) -> str:
    """Encode the dominant concept cluster as a reversible base-62 bitmask.

    Each bit corresponds to a domain in _DOMAIN_LIST (fixed order).
    The result is a short string that can be decoded back to domain names
    by decode_concept_hash() — no lookup table required.
    """
    bitmask = 0
    for d in dominant_domains[:3]:
        if d in _DOMAIN_LIST:
            bitmask |= 1 << _DOMAIN_LIST.index(d)
    if bitmask == 0:
        return "0"
    result = ""
    n = bitmask
    while n:
        result = _BASE62_CHARS[n % 62] + result
        n //= 62
    return result


def decode_concept_hash(h: str) -> List[str]:
    """Decode a base-62 bitmask back to domain names.

    Inverse of _concept_hash().  Given the short hash that appears in a
    reflection title (e.g. '4Bg'), returns the list of active domain names
    (e.g. ['identity', 'technology', 'morality']).
    """
    if h == "0":
        return []
    bitmask = 0
    for char in h:
        bitmask = bitmask * 62 + _BASE62_CHARS.index(char)
    return [_DOMAIN_LIST[i] for i in range(len(_DOMAIN_LIST)) if bitmask & (1 << i)]


def encode(text: str) -> ConceptFingerprint:
    """Encode text into its language component fingerprint.

    The shape of this fingerprint is similar to the concept's hash in the system â€”
    consistent structure, but frequency and amplitude are affected by state superimposition.
    """
    tokens = [t.lower() for t in re.split(r"\W+", text) if t and len(t) > 1]
    content_tokens = [t for t in tokens if t not in _STOPWORDS]

    domain_scores: Dict[str, float] = {d: 0.0 for d in CONCEPT_DOMAINS}

    for token in content_tokens:
        for domain, keywords in CONCEPT_DOMAINS.items():
            for kw in keywords:
                if token == kw or token.startswith(kw) or kw.startswith(token):
                    domain_scores[domain] += 1.0
                    break

    # Phrase-level matches (stronger signal)
    text_lower = text.lower()
    for domain, keywords in CONCEPT_DOMAINS.items():
        for kw in keywords:
            if " " in kw and kw in text_lower:
                domain_scores[domain] += 2.0

    max_score = max(domain_scores.values()) if domain_scores else 1.0
    normalised = {d: s / max_score for d, s in domain_scores.items()} if max_score > 0 else domain_scores

    dominant = [d for d in sorted(normalised, key=lambda d: normalised[d], reverse=True)[:3] if normalised[d] > 0]

    return ConceptFingerprint(
        domains=normalised,
        raw_tokens=content_tokens,
        dominant_domains=dominant,
        concept_hash=_concept_hash(dominant),
    )


