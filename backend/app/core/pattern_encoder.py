# State extractor â€” text structure â†’ StateFingerprint
# ---------------------------------------------------------------------------

def extract_state(text: str) -> StateFingerprint:
    """Extract the state component superimposed on the language signal.

    State signals:
    - Urgency:        fragmentation (short words/sentences), typos, abbreviations,
                      lack of punctuation, rapid short messages
    - Confusion:      question marks, hedging words, contradictions, restarts
    - Intensity:      ALL_CAPS, exclamation marks, emphasis words, repetition
    - Cognitive load: very long sentences, complex multi-clause structure,
                      many concepts packed together
    """
    words = text.split()
    sentences = re.split(r'[.!?]+', text.strip())
    sentences = [s.strip() for s in sentences if s.strip()]

    word_count = max(len(words), 1)
    char_count = max(len(text), 1)

    # --- Urgency signals ---
    avg_word_len = sum(len(w) for w in words) / word_count
    short_word_ratio = sum(1 for w in words if len(w) <= 3) / word_count
    # Typo proxy: words with repeated consonants or unusual character combos
    typo_like = sum(1 for w in words if re.search(r'[bcdfghjklmnpqrstvwxyz]{3,}', w.lower())) / word_count
    no_punct = 1.0 if not re.search(r'[.!?,;:]', text) and word_count > 5 else 0.0
    avg_sentence_len = sum(len(s.split()) for s in sentences) / max(len(sentences), 1)
    short_sentences = 1.0 if avg_sentence_len < 6 else (0.5 if avg_sentence_len < 10 else 0.0)

    urgency = min(1.0, (short_word_ratio * 0.3 + typo_like * 0.3 + no_punct * 0.2 + short_sentences * 0.2))

    # --- Confusion signals ---
    question_count = text.count("?")
    hedges = sum(1 for w in ["maybe", "perhaps", "not sure", "wonder", "unclear",
                              "confus", "dont know", "don't know", "unsure", "might",
                              "could be", "i think"] if w in text.lower())
    confusion = min(1.0, (question_count * 0.15 + hedges * 0.15))

    # --- Intensity signals ---
    caps_ratio = sum(1 for c in text if c.isupper()) / char_count
    exclamation_count = text.count("!")
    emphasis_words = sum(1 for w in ["very", "really", "absolutely", "must", "critical",
                                      "important", "always", "never", "key", "core",
                                      "exactly", "precisely"] if w in text.lower())
    intensity = min(1.0, caps_ratio * 2.0 + exclamation_count * 0.1 + emphasis_words * 0.08)

    # --- Cognitive load signals ---
    # Long sentences, many commas, multiple semicolons = high cognitive load / thinking mode
    comma_density = text.count(",") / word_count
    long_sentence_ratio = sum(1 for s in sentences if len(s.split()) > 20) / max(len(sentences), 1)
    cognitive_load = min(1.0, comma_density * 1.5 + long_sentence_ratio * 0.8)

    # --- Overall activation (how far from 'resting state') ---
    activation = min(1.0, (urgency + confusion + intensity + cognitive_load) / 2.5)

    # --- Dominant state label ---
    if urgency > 0.5 and intensity > 0.3:
        dominant = "intense"
    elif urgency > 0.4:
        dominant = "urgent"
    elif confusion > 0.3:
        dominant = "seeking"
    elif cognitive_load > 0.4:
        dominant = "deep_thinking"
    elif intensity > 0.4:
        dominant = "emphatic"
    elif activation < 0.15:
        dominant = "calm"
    else:
        dominant = "engaged"

    return StateFingerprint(
        urgency=urgency,
        confusion=confusion,
        intensity=intensity,
        cognitive_load=cognitive_load,
        activation=activation,
        dominant_state=dominant,
    )


def decompose(text: str) -> Tuple[ConceptFingerprint, StateFingerprint]:
    """Decompose an input signal into its language and state components.

    Input = language âŠ• state.
    Returns both independently so the mind can address each.
    """
    return encode(text), extract_state(text)

def decompose_pattern(text: str, source: str = "unknown", context_trace: Optional[List[str]] = None) -> "Pattern":
    """Return a fully structured Pattern object for this input text.

    This is the single entry point the engine should use.  `decompose()` is kept
    for backward compat with existing callers that unpack the tuple directly.

    Args:
        text:          Raw input text (may include context prefix).
        source:        Which mind is decomposing (mind_name or service name).
        context_trace: List of recent turn strings used to prime context.
    """
    concept_fp, state_fp = decompose(text)
    orientation = concept_fp.dominant_domains[0] if concept_fp.dominant_domains else "unknown"
    # Weight = sum of top-3 domain scores (measure of how 'strongly' the signal
    # is expressed — a dense technical message has high weight, a vague greeting low)
    top_scores = sorted(concept_fp.domains.values(), reverse=True)[:3]
    weight = round(sum(top_scores), 4)
    return Pattern(
        message_vector=concept_fp,
        state_vector=state_fp,
        context_trace=context_trace or [],
        orientation=orientation,
        weight=weight,
        source=source,
    )

