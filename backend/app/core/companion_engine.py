# ────────────────────────── Redis Key Helpers ──────────────────────────────

def _state_key(user_id: str) -> str:
    return f"companion:user:{user_id}:state"

def _history_key(user_id: str) -> str:
    return f"companion:user:{user_id}:history"

def _session_key(session_id: str) -> str:
    return f"companion:session:{session_id}:uid"

# ────────────────────────── Resonance State ───────────────────────────────

def _default_state(user_id: str) -> dict:
    return {
        "user_id":          user_id,
        "stage":            0,
        "stage_name":       "dormant",
        "resonance":        0.0,
        "dominant_pattern": "curious",
        "blocks":           [],
        "openings":         [],
        "keywords":         {},
        "message_count":    0,
        "last_seen":        time.time(),
        "induction":        None,
    }


async def get_state(r: aioredis.Redis, user_id: str) -> dict:
    raw = await r.get(_state_key(user_id))
    if raw:
        return json.loads(raw)
    state = _default_state(user_id)
    await save_state(r, user_id, state)
    return state


async def save_state(r: aioredis.Redis, user_id: str, state: dict) -> None:
    state["last_seen"] = time.time()
    await r.set(_state_key(user_id), json.dumps(state), ex=86400 * 30)


async def get_history(r: aioredis.Redis, user_id: str, limit: int = 12) -> list[dict]:
    """Return last `limit` turns as [{role, content}]."""
    raw = await r.lrange(_history_key(user_id), -limit * 2, -1)
    history = []
    for item in raw:
        try:
            history.append(json.loads(item))
        except Exception:
            pass
    return history


async def append_history(r: aioredis.Redis, user_id: str, role: str, content: str) -> None:
    key = _history_key(user_id)
    await r.rpush(key, json.dumps({"role": role, "content": content}))
    await r.ltrim(key, -60, -1)   # keep last 30 turns
    await r.expire(key, 86400 * 7)


