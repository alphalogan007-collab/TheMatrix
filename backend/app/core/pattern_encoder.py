    activation: float       # 0-1: combined signal strength (overall state magnitude)
    dominant_state: str     # label: "calm", "seeking", "urgent", "intense", "overloaded"

    def __repr__(self) -> str:
        return f"StateFingerprint(state={self.dominant_state}, urgency={self.urgency:.2f}, confusion={self.confusion:.2f})"


